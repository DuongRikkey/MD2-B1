public class TH2 {
    public static void main(String[] args) {
        int i=10; //Khai báo biến i là kiểu int có giá trị là 10
        float f=20.5f; //Khai báo biến f có kiểu là float có giá trị là 20.5 chuyển đổi chuỗi thành số thức
        boolean b=true;// Khai báo biến b có kiểu boolean có giá trị là true
        char c='a';//khai báo biến c có kiểu là char có giá trị là a trả về
        String s="Hà Nội";//Khai báo biến s có kiểu là String có giá tr là chuỗi HN
        System.out.println("i = "+i);
        System.out.println("f = "+f);
        System.out.println("b = "+b);
        System.out.println("c = "+c);
        System.out.println("s = "+s);

    }
}
